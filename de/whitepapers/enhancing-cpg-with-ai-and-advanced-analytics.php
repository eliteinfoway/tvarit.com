            <div class="text-big60">
                VERBESSERUNG VON CPG MIT KI UND ADVANCED ANALYTICS
            </div>

            <br>

            <p class="text-medium fw-700">
                With the ocean full of ships and the sails set high, it's impeccable for the companies to shine up their
                cannons. With the flooding of Consumer Packaged Goods ( CPG) market, the smarter pirates have allied
                with AI and Advanced Analytics to regain the edge. In a study conducted, companies which resorted to
                advanced analytics gained a 10% edge over others.
            </p>

            <br>

            <h4>DIE ÜBERARBEITUNG VON KI- UND ERWEITERTEN ANALYSEVERFAHREN</h4>

            <p class="text-p">
                Früher wurden die gesammelten Datensätze nie als wertvolles Gut angesehen. Mit dem Einsatz von Analysen,
                traditionellen Unternehmensdaten, Verbraucherdaten und Partnerdaten wurde jedoch alles explizit
                verbraucht, um
                Prognosen zu generieren. Vorhersagen wie die Nachfrage nach Produkten, Markttrends, die Auswirkungen
                einer
                Marketingkampagne und das Kundenverhalten.
            </p>

            <h5 class="mb-3">Bei der Aufschlüsselung der Vorhersagen wurden die vorteilhaftesten Anwendungen im CPG
                identifiziert als:</h5>
            <ul class="ul-list-bullet">
                <li>Nachfrageprognose für neue und bestehende Produkte</li>
                <li>ROI-Messung für Vertrieb und Marketing</li>
                <li>Identifizierung von Soll-Verkaufsstellen.</li>
                <li>Optimierung der Produkte, auch auf der Ebene der einzelnen Filialen.</li>
                <li>Einblicke in Kundentrends</li>
            </ul>

            <br>
            <h4>HINDERNISSE BEI DER EXPANSION</h4>
            <p class="text-p">
                Da die Veränderungen so bedeutsam und übergreifend sind, gibt es mehrere Hindernisse bei der
                Implementierung von Advanced Analytics. Jede einzelnen aktuellen Entscheidungsprozesse, automatisierte
                manuelle Aufgaben und Berechnungen und bestehende Geschäftsprozesse, müssen sich ändern. Außerdem
                wird für die Umsetzung ein neuer Satz qualifizierter und talentierter Arbeitskräfte benötigt.
            </p>
            <h5 class="mb-3">Unter Berücksichtigung aller Faktoren wurden folgende Probleme identifiziert:</h5>
            <ul class="ul-list-bullet">
                <li>Die verstreute Umsetzung und die Sandy-Planung waren eine wichtige Grundlage für die zunehmende
                    Beteiligung von KI und Advanced Analytics an CPG.</li>
                <li>Aufgrund einer unübersichtlichen Struktur, wie die Dinge weiterlaufen sollen, hat die Industrie
                    nicht die notwendigen Anstrengungen in die Industrialisierung, Skalierung und Einführung der
                    Anwendung unternommen.</li>
                <li>Der Mangel an qualifizierten Arbeitskräften in der KI hat auch zu einem großen Rückschlag geführt.
                </li>
                <li>Aufgrund der begrenzten Data Governance gibt es keine bestimmten Prozesse für Datenmanagement,
                    Qualität, Eigentum oder vielmehr gemeinsame Datentaxonomien, um die Skalierung zu erleichtern.</li>
                <li>Die Unkenntnis, wie sich digitale Ökosysteme, Datenverfügbarkeit, Kanaldynamik und
                    Anbieterfähigkeiten im Vergleich zu den Märkten unterscheiden, hat zu erheblichen Marktverzerrungen
                    geführt.</li>
            </ul>

            <br>
            <h4>VORSCHLÄGE FÜR EINE SCHNELLE IMPLEMENTIERUNG</h4>
            <h5 class="mb-3">Vor der Skalierung von KI und Advanced Analytics in CPG können die folgenden Schritte für
                eine schnellere Implementierung berücksichtigt werden:</h5>
            <ul class="ul-list-bullet">
                <li>Filterung der Optionen - für besseren Fokus und weniger Streuung; die CPGs sollten zählbare
                    Anwendungen entwickeln. </li>
                <li>Identifizierung der Patenschaft - bevor sie tatsächlich eintritt; es ist entscheidend, dass die CPG
                    Unternehmen
                    identifiziert, die ihre Anwendung drehen. Marken sollten auch funktionieren um zu gewährleisten,
                    dass es ein
                    bemerkenswertes Interesse unter den lokalen Mitgliedern gibt, die die Einführung und das Marketing
                    der
                    Anwendung unterstützen.</li>
                <li>Marktspezifische Bedürfnisse adressieren - je früher diese angesprochen werden und je schneller die
                    Probleme erkannt werden, desto besser können neue Dienstleistungen von den Marken umgesetzt werden.
                </li>
                <li>Die Vorbereitung vor dem Showdown - die Zusammenführung von KI und Advanced Analytics - erfordert
                    den Aufbau neuer technischer Ökosysteme. Die Ernährung, das Hosting und die Anpassung an die neuen
                    Ökosysteme können schwierig sein und erfordern eine vorherige Unterstützung.</li>
            </ul>

            <br>

            <p class="text-p">
            Die Ziele und Standards sind hoch gesteckt. Um sie zu erreichen, ist ein hierarchisches Clustering erforderlich, 
            um alle Ebenen von der Entscheidungsfindung auf breiter Ebene bis zum Shop-Floor-Management zu nutzen. Für 
            einen guten Start, CPG-Unternehmen sollten mit einer Handvoll Investitionen beginnen und ihren Nexus 
            schrittweise ausbauen.
            </p>

