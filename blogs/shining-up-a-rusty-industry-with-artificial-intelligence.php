        <p class="text-p">
         Machine learning models can foretell when breakdowns are most likely to occur and minimize their occurrence. All steel mills have unplanned circumstances like breakouts (when molten steel breaks out of a mold during casting) and cobbles (when hot rolled steel escapes from rollers, often onto the mill floor). These events hold production and are both risky and expensive. 
        </p>
